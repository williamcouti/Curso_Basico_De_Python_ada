# Operações Matematicas (Aritimeticas)

"""
-Soma: +
-Subtração: - 
-Multiplicações: *
-Divisão: /
-Divisão Inteira: //
-Resto da divisão: %
-Potencia: **
"""

numero1 = 10
numero2 = 20

print(numero1 + numero2)  #Soma
print(numero1 - numero2)  #Subtração
print(numero1 * numero2)  #Multiplicação
print(numero1 / numero2)  #Divisão
print(numero1 // numero2)  #Divisão inteiro 
print(numero1 % numero2)   #Resto da Divisão
print(numero1 ** numero2)  #Elevado ao cubo 2

#Operadores Booleanos

