nome = 'Willian coutinho'

print(nome)

"""
Tipos de varáveis

1. int: números inteiros, ou seja números sem parte decimal: 0, 5, -1, 1000.
2. float: números reais, ou seja números com parte decimal: 1.0, -2.7,, 3.14.
3. str: cadeias de caracteres, ou seja, dados textuais.
4. bool: valores lógicos (booleanos): que retona true ou false.
"""

idade2 = 30
altura = 1.80
nome = 'Willian Coutinho'
estudando = True

#type retorna o tipo do objeto no caso da variavel
print(type(idade2))
print(type(altura))
print(type(nome))
print(type(estudando))

